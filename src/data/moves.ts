export function getMoveNameCN(en: string): string {
  const direct = MOVE_NAMES[en];
  if (direct) return direct;
  // 去掉 [] 后缀再尝试
  const base = en.split(/\s*\[.*?\]/)[0].trim();
  if (base !== en && MOVE_NAMES[base]) return MOVE_NAMES[base];
  // 大小写不敏感匹配
  const lower = en.toLowerCase();
  for (const key of Object.keys(MOVE_NAMES)) {
    if (key.toLowerCase() === lower) return MOVE_NAMES[key];
  }
  return en;
}
